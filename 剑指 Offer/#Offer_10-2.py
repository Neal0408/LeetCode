# 剑指 Offer 10-2.青蛙跳台阶问题
# 一只青蛙以此可以跳上 1 级台阶，也可以跳上 2 级台阶。求该青蛙跳上一个 n 级台阶总共有多少种
# 跳法。
# 解题思路
# 1.跟斐波那契数列一个道理。这边想一下想要跳上 n 级台阶，那么前一步会有多少种方法呢。可能为
# f(n-1)，也可能为 f(n-2)。所以也就可以推出 f(n) = f(n-1) + f(n-2)。但是初始状态与菲波
# 那切数列不太一样。


class Solution:

    def numWays(self, n: int) -> int:
        a, b = 1, 1
        for _ in range(n):
            a, b = b, a + b
        return a % 1000000007


obj = Solution()
