# 剑指 Offer 10-1.菲波那切数列
# 写一个函数，输入 n，求菲波那切数列的第 n 项。
# 解题思路
# 1.最经典的动态规划的习题之一，隐隐约约的涵盖了动态规划的思想。动态规划主要有这么几个步骤，
# 状态定义、初始状态、转移方程、返回值。然后解题的步骤就是不断重叠子问题。初始状态实现对问题
# 的迭代求解。


class Solution:

    def fib(self, n: int) -> int:
        a, b = 0, 1
        for i in range(n):
            a, b = b, a + b
        return a % 1000000007


obj = Solution()
